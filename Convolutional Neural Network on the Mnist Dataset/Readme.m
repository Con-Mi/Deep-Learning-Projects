# Details
This code builds a Convolutional neural network with 2 convolutional layers, with rectified linear units for activation 
and two fully connected layers again with rectified linear units for activation.
Dropout is also implemnted before the final fully connected layer.
Optimization of the cross-entropy function is done with the Adam Optimizer. 
Implementation is done using the tensorflow library.
Run time on a nVidia 2Gb Gpu is approximately 20 mins(bash timing), with an accuracy around 99.2%. 
